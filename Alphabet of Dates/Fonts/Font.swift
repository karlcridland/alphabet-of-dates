import SwiftUI

extension Font {
    
    static func playfairDisplay(size: CGFloat) -> Font {
        Font.custom("PlayfairDisplay-Black", size: size)
    }
    
    static func lerckerliOne(size: CGFloat) -> Font {
        Font.custom("LeckerliOne-Regular", size: size)
    }
    
}
